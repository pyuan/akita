<h2>Destroy</h2>
<p>Any akita speech bubble attached to a DOM element can be destroyed by passing in the <span class="highlight">"destroy"</span> string.</p>

<pre class="brush: javascript;">
	
	/**
	 * destroy the akita speech bubble that had been previously attached to a DOM element
	 */
	var ele = $("css selector");
	$(ele).akita("destroy");

</pre>



